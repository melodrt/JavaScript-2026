const numero1 = 37;
const numero2 = 25;

let resultado;
console.log(resultado);

// suma
resultado = numero1 + numero2;
console.log(`suma: ${resultado}`);

// resta
resultado = numero1 - numero2;
console.log(`resta: ${resultado}`);

// multiplicacion
resultado = numero1 * numero2;
console.log(`multiplicacion: ${resultado}`);

// division
resultado = numero1 / numero2;
console.log(`division: ${resultado}`);

// modulo
resultado = numero1 % numero2;
console.log(`modulo: ${resultado}`);


